grade = input("please enter your letter grade (A, B, C, D, or F): ")


if grade == 'A':
        print("excellent!")
elif grade == 'B':
        print("good job!")
elif grade == 'C':
        print("you passed.")
elif grade == 'D':
        print("you can do better.")
elif grade == 'F':
        print("you failed.")
else:
        print("invalid grade.")